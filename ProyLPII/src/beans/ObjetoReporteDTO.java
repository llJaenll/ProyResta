package beans;

public class ObjetoReporteDTO {
	private String nombre;
	private int cant;
	
	public ObjetoReporteDTO(){
		
	}

	public String getNombre() {
		return nombre;
	}

	public void setNombre(String nombre) {
		this.nombre = nombre;
	}

	public int getCant() {
		return cant;
	}

	public void setCant(int cant) {
		this.cant = cant;
	}
	
	

}
